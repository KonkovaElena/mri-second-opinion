const ssot = JSON.parse(readFileSync(ssotPath, "utf-8"));

function assertRouteDescriptor(route) {
  if (!route || typeof route.method !== "string" || typeof route.path !== "string" || typeof route.description !== "string") {
    throw new Error(`Invalid route descriptor in ${ssotPath}: ${JSON.stringify(route)}`);
  }
}

for (const category of ["publicWorkflow", "internalIntegration", "operational"]) {
  for (const route of ssot.routeSurface[category]) {
    assertRouteDescriptor(route);
  }
}

function formatRoute(route) {
  return `${route.method} ${route.path}`;
}

function quote(value) {
  return JSON.stringify(value);
}

function generateApiSurfaceTs() {
  const { publicWorkflow, internalIntegration, operational } = ssot.routeSurface;
  const serializeRoutes = (routes) => routes
    .map(
      (route) =>
        `  { method: ${quote(route.method)}, path: ${quote(route.path)}, description: ${quote(route.description)} },`,
    )
    .join("\n");

  return `// AUTO-GENERATED by scripts/sync-authority-docs.mjs. Do not edit by hand.\n\nexport interface ApiRouteDescriptor {\n  method: string;\n  path: string;\n  description: string;\n}\n\nexport const PUBLIC_WORKFLOW_ROUTES: readonly ApiRouteDescriptor[] = [\n${serializeRoutes(publicWorkflow)}\n];\n\nexport const INTERNAL_INTEGRATION_ROUTES: readonly ApiRouteDescriptor[] = [\n${serializeRoutes(internalIntegration)}\n];\n\nexport const OPERATIONAL_ROUTES: readonly ApiRouteDescriptor[] = [\n${serializeRoutes(operational)}\n];\n\nexport function formatRouteDescriptor(route: ApiRouteDescriptor): string {\n  return \`${"${route.method}"} ${"${route.path}"}\`;\n}\n\nexport const PUBLIC_WORKFLOW_ROUTE_STRINGS = PUBLIC_WORKFLOW_ROUTES.map(formatRouteDescriptor);\nexport const INTERNAL_INTEGRATION_ROUTE_STRINGS = INTERNAL_INTEGRATION_ROUTES.map(formatRouteDescriptor);\nexport const OPERATIONAL_ROUTE_STRINGS = OPERATIONAL_ROUTES.map(formatRouteDescriptor);\n\nexport const API_SURFACE_SUMMARY = {\n  publicWorkflowCount: PUBLIC_WORKFLOW_ROUTES.length,\n  internalIntegrationCount: INTERNAL_INTEGRATION_ROUTES.length,\n  operationalCount: OPERATIONAL_ROUTES.length,\n  totalApiCount: PUBLIC_WORKFLOW_ROUTES.length + INTERNAL_INTEGRATION_ROUTES.length,\n  totalHttpSurfaceCount: PUBLIC_WORKFLOW_ROUTES.length + INTERNAL_INTEGRATION_ROUTES.length + OPERATIONAL_ROUTES.length,\n} as const;\n`;
}

function gitTrackedFiles() {
  const result = spawnSync("git", ["ls-files"], { cwd: repoRoot, encoding: "utf-8" });
  if (result.status !== 0) {
    throw new Error(result.stderr || "git ls-files failed");
  }

  return result.stdout
    .split(/\r?\n/u)
    .map((line) => line.trim())
    .filter((line) => line.length > 0);
}

function countLines(filePath) {
  const text = readFileSync(join(repoRoot, filePath), "utf-8");
  return text.length === 0 ? 0 : text.split(/\r?\n/u).length;
}

function collectTrackedMetrics() {
  const files = gitTrackedFiles();
  const srcTsFiles = files.filter((file) => file.startsWith("src/") && file.endsWith(".ts"));
  const workerPyFiles = files.filter((file) => file.startsWith("worker/") && file.endsWith(".py"));
  const testFiles = files.filter((file) => file.startsWith("tests/") && file.endsWith(".test.ts"));
  const docMarkdownFiles = files.filter((file) => file.startsWith("docs/") && file.endsWith(".md"));
  const docBinaryFiles = files.filter((file) => file.startsWith("docs/") && !file.endsWith(".md") && !file.endsWith(".json"));
  const docBytes = [...docMarkdownFiles, ...docBinaryFiles].reduce((total, file) => total + readFileSync(join(repoRoot, file)).byteLength, 0);

  return {
    srcTs: { files: srcTsFiles.length, loc: srcTsFiles.reduce((total, file) => total + countLines(file), 0) },
    workerPy: { files: workerPyFiles.length, loc: workerPyFiles.reduce((total, file) => total + countLines(file), 0) },
    tests: { files: testFiles.length, loc: testFiles.reduce((total, file) => total + countLines(file), 0) },
    docs: {
      markdownFiles: docMarkdownFiles.length,
      binaryFiles: docBinaryFiles.length,
      totalFiles: docMarkdownFiles.length + docBinaryFiles.length,
      totalMegabytes: (docBytes / (1024 * 1024)).toFixed(1),
    },
  };
}

function replaceBlock(contents, marker, replacement) {
  const start = `<!-- ${marker}:START -->`;
  const end = `<!-- ${marker}:END -->`;
  const pattern = new RegExp(`${escapeRegex(start)}[\\s\\S]*?${escapeRegex(end)}`, "u");

  if (!pattern.test(contents)) {
    throw new Error(`Missing marker ${marker} in target file`);
  }

  return contents.replace(pattern, `${start}\n${replacement.trimEnd()}\n${end}`);
}

function escapeRegex(value) {
  return value.replace(/[.*+?^${}()|[\]\\]/gu, "\\$&");
}

function renderRouteTable(routes) {
  const lines = ["| Method | Path | Description |", "|---|---|---|"];
  for (const route of routes) {
    lines.push(`| \`${route.method}\` | \`${route.path}\` | ${route.description} |`);
  }
  return lines.join("\n");
}

function renderNumberedRouteList(routes, startIndex = 1) {
  return routes.map((route, index) => `${startIndex + index}. \`${formatRoute(route)}\``).join("\n");
}

function writeOrCheck(targetPath, expectedContents) {
  const relativePath = relative(repoRoot, targetPath).replace(/\\/gu, "/");
  const currentContents = readFileSync(targetPath, "utf-8");

  if (currentContents === expectedContents) {
    return;
  }

  if (checkMode) {
    throw new Error(`Authority drift detected in ${relativePath}. Run: node scripts/sync-authority-docs.mjs`);
  }

  writeFileSync(targetPath, expectedContents, "utf-8");
}

function updateMarkdownFile(relativePath, transform) {
  const absolutePath = join(repoRoot, relativePath);
  const currentContents = readFileSync(absolutePath, "utf-8");
  const nextContents = transform(currentContents);
  writeOrCheck(absolutePath, nextContents);
}

